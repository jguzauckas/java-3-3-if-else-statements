public class NotesIf1 {
    public static void main(String[] args) {
        int num = 10;

        if (num > 15) {
            System.out.println(num + " is greater than 15.");
        } 
        if (num <= 15) {
            System.out.println(num + " is less than or equal to 15.");
        }
        System.out.println("num is " + num + ".");
    }
}
